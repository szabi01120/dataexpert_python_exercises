def is_prime(n):
    if n <= 1:
        return False
    if n <= 3:
        return True
    if n % 2 == 0 or n % 3 == 0:
        return False
    i = 5
    while i * i <= n:
        if n % i == 0 or n % (i + 2) == 0:
            return False
        i += 6
    return True

def find_prime_factors_of_length(length):
    for i in range(2, length + 1):
        if length % i == 0 and is_prime(i):
            factor = length // i
            if is_prime(factor):
                return i, factor
    return None

